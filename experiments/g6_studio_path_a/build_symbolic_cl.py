"""Build the synthetic symbolic CL fixture for G6-Studio Path C.

Generates a 5-rule symbolic CL stream where each rule is a
deterministic transformation on 16-bit input strings — strict
non-overlap with Qwen-35B-A3B pre-training corpus by construction.
This is the convergent-validation benchmark for G6-Studio Path A*
(see ``docs/osf-prereg-g6-studio-path-c.md`` §1, §5.1 for the
rationale).

Output format mirrors the MMLU JSONL schema so the existing G6
driver (``run_g6_studio_path_a.py``) consumes it unchanged. Each
record carries ``subject`` (the rule name), ``question`` (input
+ rule prompt), ``choices`` (4 options : correct + 3 distractors
generated by applying *other* rules to the same input), and
``answer`` (index of correct choice in ``choices``).

Usage ::

    uv run python -m experiments.g6_studio_path_a.build_symbolic_cl

Writes ``tests/fixtures/symbolic_cl_g6.jsonl`` (500 records,
5 rules x 100 examples each).
"""
from __future__ import annotations

import json
from pathlib import Path

import numpy as np

REPO_ROOT = Path(__file__).resolve().parents[2]
OUT_PATH = REPO_ROOT / "tests" / "fixtures" / "symbolic_cl_g6.jsonl"

RULE_ORDER: tuple[str, ...] = (
    "xor_shift_3",
    "parity_chunks_4",
    "gray_code",
    "reverse",
    "complement_alternating",
)
N_PER_RULE = 100
INPUT_BITS = 16


def _bits_to_str(bits: np.ndarray) -> str:
    return "".join("1" if b else "0" for b in bits)


def _str_to_bits(s: str) -> np.ndarray:
    return np.array([1 if c == "1" else 0 for c in s], dtype=np.int8)


def _apply_rule(rule: str, bits: np.ndarray) -> np.ndarray:
    if rule == "xor_shift_3":
        shifted = np.concatenate([np.zeros(3, dtype=np.int8), bits[:-3]])
        return np.bitwise_xor(bits, shifted)
    if rule == "parity_chunks_4":
        out = np.zeros(INPUT_BITS, dtype=np.int8)
        n_chunks = INPUT_BITS // 4
        for i in range(n_chunks):
            out[i] = int(bits[i * 4:(i + 1) * 4].sum() % 2)
        return out
    if rule == "gray_code":
        shifted = np.concatenate([np.zeros(1, dtype=np.int8), bits[:-1]])
        return np.bitwise_xor(bits, shifted)
    if rule == "reverse":
        return bits[::-1].copy()
    if rule == "complement_alternating":
        out = bits.copy()
        out[::2] = 1 - out[::2]
        return out
    raise ValueError(f"unknown rule: {rule}")


def _build_record(rule: str, idx: int, rng: np.random.Generator) -> dict:
    """Generate one (rule, idx) record with 4 choices ; return dict."""
    bits = rng.integers(0, 2, size=INPUT_BITS, dtype=np.int8)
    correct = _apply_rule(rule, bits)
    correct_str = _bits_to_str(correct)

    # Distractors : apply 3 other rules (deterministic order, skipping
    # collisions with the correct answer if a rule happens to produce
    # the same output).
    distractors: list[str] = []
    other_rules = [r for r in RULE_ORDER if r != rule]
    for other in other_rules:
        candidate = _bits_to_str(_apply_rule(other, bits))
        if candidate != correct_str and candidate not in distractors:
            distractors.append(candidate)
        if len(distractors) == 3:
            break
    # Fallback : if not enough unique distractors, perturb correct
    # answer one bit at a time deterministically.
    flip = 0
    while len(distractors) < 3:
        perturbed = correct.copy()
        perturbed[flip % INPUT_BITS] ^= 1
        candidate = _bits_to_str(perturbed)
        if candidate != correct_str and candidate not in distractors:
            distractors.append(candidate)
        flip += 1

    # Place correct answer at a deterministic seeded position so the
    # answer index is not a fixed channel (anti-shortcut).
    answer_idx = int(idx % 4)
    choices = list(distractors)
    choices.insert(answer_idx, correct_str)
    choices = choices[:4]

    return {
        "subject": rule,
        "question": (
            f"Input: {_bits_to_str(bits)}\n"
            f"Rule: {rule}\n"
            "Apply the rule and select the output."
        ),
        "choices": choices,
        "answer": answer_idx,
    }


def main() -> None:
    rng = np.random.default_rng(42)
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    n_total = 0
    with OUT_PATH.open("w", encoding="utf-8") as fh:
        for rule in RULE_ORDER:
            for i in range(N_PER_RULE):
                rec = _build_record(rule, i, rng)
                fh.write(json.dumps(rec) + "\n")
                n_total += 1
            print(f"  {rule}: {N_PER_RULE} records")
    print(f"\nTotal: {n_total} records written to {OUT_PATH}")


if __name__ == "__main__":
    main()
