# --------------------------------------------------------------------------
# Test 1 — loader determinism (every loader)
# --------------------------------------------------------------------------


def test_loader_determinism_across_all_three(
    mmlu_fixture: tuple[Path, DatasetPin],
    hellaswag_fixture: tuple[Path, DatasetPin],
    mega_v2_fixture: Path,
) -> None:
    """Same pin + seed + fixture → identical record sequences.

    Covers all three loaders in one property test ; determinism is
    the R1 contract and must hold uniformly.
    """
    mmlu_path, mmlu_pin = mmlu_fixture
    hs_path, hs_pin = hellaswag_fixture

    a = list(
        MMLULoader(mmlu_pin, local_path=mmlu_path).iter_records(seed=7)
    )
    b = list(
        MMLULoader(mmlu_pin, local_path=mmlu_path).iter_records(seed=7)
    )
    assert a == b

    a_hs = list(
        HellaSwagLoader(hs_pin, local_path=hs_path).iter_records()
    )
    b_hs = list(
        HellaSwagLoader(hs_pin, local_path=hs_path).iter_records()
    )
    assert a_hs == b_hs

    a_m = MegaV2EvalLoader(local_path=mega_v2_fixture).train_eval_split(
        eval_fraction=0.2, seed=42
    )
    b_m = MegaV2EvalLoader(local_path=mega_v2_fixture).train_eval_split(
        eval_fraction=0.2, seed=42
    )
    assert a_m == b_m


# --------------------------------------------------------------------------
# Test 2 — SHA-256 verification against registry
# --------------------------------------------------------------------------


def test_sha256_verification_against_registry(
    tmp_path: Path,
    mmlu_fixture: tuple[Path, DatasetPin],
) -> None:
    """Loader stores a SHA-256 of the local file and refuses to
    treat it as registry-verified unless the caller supplies the
    matching expected digest.

    R1 ban-silent-drift : if a caller passes an ``expected_sha256``
    that does not match the actual file, loader raises
    :class:`ValueError`.
    """
    mmlu_path, pin = mmlu_fixture
    loader = MMLULoader(pin, local_path=mmlu_path)
    actual = loader.local_file_sha256()
    assert isinstance(actual, str)
    assert len(actual) == 64

    # Matching digest : no raise, loader reports verified=True.
    loader_ok = MMLULoader(
        pin, local_path=mmlu_path, expected_sha256=actual
    )
    assert loader_ok.hash_verified is True

    # Mismatched digest : raise.
    bad = "f" * 64
    with pytest.raises(ValueError, match="sha256 mismatch"):
        MMLULoader(pin, local_path=mmlu_path, expected_sha256=bad)


# --------------------------------------------------------------------------
# Test 3 — schema validation (typed records)
# --------------------------------------------------------------------------


def test_schema_validation_typed_records(
    mmlu_fixture: tuple[Path, DatasetPin],
    hellaswag_fixture: tuple[Path, DatasetPin],
    mega_v2_fixture: Path,
) -> None:
    """Loaders emit the declared record dataclasses with the
    documented field types."""
    mmlu_path, mmlu_pin = mmlu_fixture
    hs_path, hs_pin = hellaswag_fixture

    mmlu_records = list(
        MMLULoader(mmlu_pin, local_path=mmlu_path).iter_records(seed=0)
    )
    assert mmlu_records, "MMLU loader yielded no records"
    for r in mmlu_records:
        assert isinstance(r, MMLURecord)
        assert isinstance(r.question, str)
        assert isinstance(r.choices, tuple)
        assert len(r.choices) == 4
        assert 0 <= r.answer < 4
        assert isinstance(r.subject, str)

    hs_records = list(
        HellaSwagLoader(hs_pin, local_path=hs_path).iter_records()
    )
    assert hs_records
    for r in hs_records:
        assert isinstance(r, HellaSwagRecord)
        assert isinstance(r.ctx, str)
        assert isinstance(r.endings, tuple)
        assert len(r.endings) == 4
        assert 0 <= r.label < 4

    mega_records = list(
        MegaV2EvalLoader(local_path=mega_v2_fixture).iter_records()
    )
    assert mega_records
    for r in mega_records:
        assert isinstance(r, MegaV2EvalRecord)
        assert r.id.startswith("mv2-")
        assert isinstance(r.context, str)
        assert isinstance(r.expected, str)
        assert isinstance(r.domain, str)


# --------------------------------------------------------------------------
# Test 4 — MMLU 5-shot sampling reproducible under fixed seed
# --------------------------------------------------------------------------


def test_mmlu_5shot_sampling_reproducible(
    mmlu_fixture: tuple[Path, DatasetPin],
) -> None:
    """``get_5shot_exemplars(seed=S)`` is idempotent and returns
    exactly 5 exemplars. Different seeds yield different orderings.
    Spec §5 : MMLU protocol is ``5-shot``.
    """
    mmlu_path, pin = mmlu_fixture
    loader = MMLULoader(pin, local_path=mmlu_path)

    a = loader.get_5shot_exemplars(seed=123)
    b = loader.get_5shot_exemplars(seed=123)
    assert a == b
    assert len(a) == 5
    assert all(isinstance(r, MMLURecord) for r in a)

    c = loader.get_5shot_exemplars(seed=456)
    # Under a 12-record fixture + 5-sample draw, seeds 123 and 456
    # should produce different exemplar orderings.
    assert a != c


# --------------------------------------------------------------------------
# Test 5 — HellaSwag zero-shot iterator
# --------------------------------------------------------------------------


def test_hellaswag_zero_shot_iterator_yields_every_example(
    hellaswag_fixture: tuple[Path, DatasetPin],
) -> None:
    """``iter_records()`` streams every row in fixture order.

    HellaSwag protocol is zero-shot (no exemplar sampling) : the
    iterator surface is the evaluation surface.
    """
    hs_path, pin = hellaswag_fixture
    loader = HellaSwagLoader(pin, local_path=hs_path)

    records = list(loader.iter_records())
    with hs_path.open("r", encoding="utf-8") as fh:
        expected_ctx = [json.loads(line)["ctx"] for line in fh if line.strip()]
    assert [r.ctx for r in records] == expected_ctx

    # Seeded sample yields a deterministic subset.
    sample_a = loader.get_seeded_sample(seed=9, n=4)
    sample_b = loader.get_seeded_sample(seed=9, n=4)
    assert sample_a == sample_b
    assert len(sample_a) == 4


# --------------------------------------------------------------------------
# Test 6 — mega-v2 80/20 split deterministic + MissingLocalDatasetError
# --------------------------------------------------------------------------


def test_mega_v2_80_20_split_deterministic(
    mega_v2_fixture: Path,
) -> None:
    """``train_eval_split`` partitions the fixture into 80/20 shards
    with stable seeded permutation. Disjoint + cover + exact ratio
    for 50-row fixture → 40 train / 10 eval.
    """
    loader = MegaV2EvalLoader(local_path=mega_v2_fixture)
    train, eval_ = loader.train_eval_split(eval_fraction=0.2, seed=42)
    ids_train = {r.id for r in train}
    ids_eval = {r.id for r in eval_}

    assert len(train) == 40
    assert len(eval_) == 10
    assert ids_train.isdisjoint(ids_eval)
    assert ids_train | ids_eval == {f"mv2-{i:04d}" for i in range(50)}

    # Same seed : same split (order-identical).
    train2, eval_2 = loader.train_eval_split(eval_fraction=0.2, seed=42)
    assert [r.id for r in train] == [r.id for r in train2]
    assert [r.id for r in eval_] == [r.id for r in eval_2]

    # Different seed : different partition (hash-test on eval ids).
    _, eval_3 = loader.train_eval_split(eval_fraction=0.2, seed=99)
    assert {r.id for r in eval_} != {r.id for r in eval_3}


def test_missing_local_path_raises_typed_error(
    tmp_path: Path,
) -> None:
    """All three loaders raise :class:`MissingLocalDatasetError` when
    the provided local path does not exist — never silently hit HF.
    """
    pin = get_dataset_pin("mmlu")
    with pytest.raises(MissingLocalDatasetError):
        MMLULoader(pin, local_path=tmp_path / "no-mmlu.jsonl")

    pin_hs = get_dataset_pin("hellaswag")
    with pytest.raises(MissingLocalDatasetError):
        HellaSwagLoader(pin_hs, local_path=tmp_path / "no-hellaswag.jsonl")

    with pytest.raises(MissingLocalDatasetError):
        MegaV2EvalLoader(local_path=tmp_path / "no-mega-v2.jsonl")


