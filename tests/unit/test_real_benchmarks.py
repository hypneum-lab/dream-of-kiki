"""Unit tests for cycle-3 C3.1 real-benchmark loaders.

Covers ``harness.real_benchmarks.{mmlu, hellaswag, mega_v2_eval}``
with the 6-test TDD pattern mandated by the cycle-3 plan :

1. loader determinism : same pin + seed → same records
2. SHA-256 verification : loader honours the pin registry
3. schema validation : records conform to the typed record type
4. MMLU 5-shot sampling reproducible under fixed seed
5. HellaSwag zero-shot iterator surfaces every example
6. mega-v2 80/20 split deterministic

All tests are network-free : the loaders accept an explicit local
JSONL fixture path, and when that path is absent they raise the
canonical ``MissingLocalDatasetError`` (no silent HF download in
tests per R1 reproducibility). The fixture files live under
``tests/fixtures/real_benchmarks/`` and are regenerated by the
``create_fixture`` helper when missing.

Reference :
  docs/superpowers/plans/2026-04-19-dreamofkiki-cycle3-atomic.md §C3.1
  docs/superpowers/specs/2026-04-19-dreamofkiki-cycle3-design.md §5
"""
